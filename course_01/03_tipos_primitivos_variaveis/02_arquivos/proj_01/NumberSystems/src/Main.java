public class Main {
  public static void main(String[] args) {
    // Number in binary format (radix 2)
    int bin = 0b10011000;
    // Number in octal format (radix 8)
    int oct = 0230;
    // Number in decimal format (radix 10)
    int dec = 152;
    // Number in hexadecimal format (radix 16)
    // You can use the letters like 0x98AF
    int hex = 0x98;
    // Printing numbers in decimal format
    System.out.println(bin);
    System.out.println(oct);
    System.out.println(dec);
    System.out.println(hex);
  }
}